package com.vision_classes.model.subscription;

public class ItemSubscription {

    String title;
    int number;

    public ItemSubscription() {
        this.title = title;
        this.number = number;
    }

    public int getNumber() {
        return number;
    }

    public void setNumber(int number) {
        this.number = number;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }
}
